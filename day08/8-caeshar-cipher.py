alphabet = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 
            't', 'u', 'v', 'w', 'x', 'y', 'z', 'a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 
            'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']


def caesar(text, shift, direction):
    cipher_text = ""
    for i in text:
        if i not in alphabet:
            cipher_text += i
        else:
            position = alphabet.index(i)
            if direction == "encode":
                cipher_text += alphabet[position + shift]
            if direction == "decode":
                cipher_text += alphabet[position - shift]
    print(f"The {direction}-text is {cipher_text}")        


continue_caesar = True
while continue_caesar:
    direction = input("Type 'encode' to encrypt, type 'decode' to decrypt:\n")
    text = input("Type your message:\n").lower()
    shift = int(input("Type the shift number:\n"))
    shift = shift % 26
    caesar(text, shift, direction)
    restart = input("You want to restart the cipher program? 'yes' or 'no' : ")
    if restart == "no":
        continue_caesar = False
        print("Goodbye!")
